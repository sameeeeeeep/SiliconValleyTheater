    /// Returns ALL static filler sets for a theme, shuffled, as FillerSet objects.
    static func allSets(themeId: String) -> [FillerSet] {
        let tagged: [TaggedSet]
        switch themeId {
        case "gilfoyle-dinesh":  tagged = gilfoyleDineshTagged
        case "rick-morty":       tagged = rickMortyTagged
        case "sherlock-watson":  tagged = sherlockWatsonTagged
        case "chandler-joey":    tagged = chandlerJoeyTagged
        case "dwight-jim":       tagged = dwightJimTagged
        case "jesse-walter":     tagged = jesseWalterTagged
        case "tony-jarvis":      tagged = tonyJarvisTagged
        case "david-moira":      tagged = davidMoiraTagged
        case "professor-bug":    tagged = professorBugTagged
        default:                 tagged = genericTagged
        }

        return tagged.shuffled().map { set in
            let lines = set.lines.enumerated().map { i, text in
                DialogueLine(characterIndex: i % 2, text: text)
            }
            return FillerSet(lines: lines, source: .static, tags: set.tags)
        }
    }

    /// Returns all static term explainers as TermExplainerEntry objects.
    static func allTermExplainerEntries() -> [TermExplainerEntry] {
        termExplainers.compactMap { keywords, rawLines in
            guard let canonical = keywords.first else { return nil }
            let lines = rawLines.enumerated().map { i, text in
                DialogueLine(characterIndex: i % 2, text: text)
            }
            let set = FillerSet(lines: lines, source: .static, isVoiceCached: false)
            return TermExplainerEntry(canonicalTerm: canonical, keywords: keywords, fillerSet: set)
        }
    }

