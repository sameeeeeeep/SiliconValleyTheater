import Foundation

// MARK: - FillerSet

/// A set of dialogue lines that can be played as filler banter.
/// Consumed once and discarded — never replayed.
struct FillerSet: Identifiable {
    let id: UUID = UUID()
    let lines: [DialogueLine]
    let source: Source
    let tags: Set<String>         // context tags for relevance matching (e.g. "git", "deploy", "coding")
    var isVoiceCached: Bool = false

    enum Source {
        case `static`   // from FillerLibrary seed content
        case dynamic    // generated by LLM at runtime
        case theaterMd  // from project's theater.md
    }

    init(lines: [DialogueLine], source: Source, tags: Set<String> = [], isVoiceCached: Bool = false) {
        self.lines = lines
        self.source = source
        self.tags = tags
        self.isVoiceCached = isVoiceCached
    }
}

// MARK: - TermExplainerEntry

/// A keyword-triggered explainer filler, matchable by terms in events.
